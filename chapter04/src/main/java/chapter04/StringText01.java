package chapter04;

public class StringText01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		
		// "hello" 출력 \\는 제어 문자.
		System.out.println("\"hello\"");
		
		// \t은 tap
		// \r은 carrige return
		// \n은 newline
		// \b는 beep
		System.out.print("hello\tworld\n");
		System.out.println("hello\tworld");
		
		//'
		char c = '\'';
		System.out.println(c);
		
	}

}
